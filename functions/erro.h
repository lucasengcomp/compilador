void mensagemDeErro(int codigoDoErro, int numeroDaLinhaNoArquivoComErro)
{
    if(numeroDaLinhaNoArquivoComErro != 0) {
        printf("LINHA: %i\n", numeroDaLinhaNoArquivoComErro);
    }
    switch (codigoDoErro) {
        case 0: 
            printf("|**************************************************************************| \n");
            printf("|========== Erro 0 - A declaracao do Main está incorreto. =================| \n");
            printf("|__________________________________________________________________________| \n");
            break;

        case 1: 
            printf("|**************************************************************************| \n");
            printf("|========== Erro 1 - O Módulo Principal é Inexistente =====================| \n");
            printf("|__________________________________________________________________________| \n");
            break;

        case 2:
            printf("|**************************************************************************| \n");
            printf("|========== Erro 2 - O limitador de tamanho contem símbolo inválido =======| \n");
            printf("|__________________________________________________________________________| \n");
            break;

        case 3:
            printf("|**************************************************************************| \n");
            printf("|========== Erro 3 - Declaração de Main está duplicado ====================| \n");
            printf("|__________________________________________________________________________| \n");
            break;

        case 4:
            printf("|**************************************************************************| \n");
            printf("|========== Erro 4 - Nome da Function contém símbolo inválido =============| \n");
            printf("|__________________________________________________________________________| \n");
            break;

         case 5:
            printf("|**************************************************************************| \n");
            printf("|========== Erro 5 - A declaração da Function está incorreta ==============| \n");
            printf("|__________________________________________________________________________| \n");
            break;

        case 6:
            printf("|**************************************************************************| \n");
            printf("|========== Erro 6 - A declaracao do Integer está incorreto ===============| \n");
            printf("|__________________________________________________________________________| \n");
            break;

        case 7:
            printf("|**************************************************************************| \n");
            printf("|========== Erro 7 - Nome da Variavel não comecou com marcador '$' ========| \n");
            printf("|__________________________________________________________________________| \n");
            break;

        case 8:
            printf("|**************************************************************************| \n");
            printf("|========== Erro 8 - Nome da variável contém símbolo inválido =============| \n");
            printf("|__________________________________________________________________________| \n");
            break;

        case 9:
            printf("|**************************************************************************| \n");
            printf("|========== Erro 9 - Declaração do Char está incorreto ====================| \n");
            printf("|__________________________________________________________________________| \n");
            break;

        case 10:
            printf("|**************************************************************************| \n");
            printf("|========== Erro 10 - A declaração do Float está incorreta ================| \n");
            printf("|__________________________________________________________________________| \n");
            break;

        case 11:
            printf("|**************************************************************************| \n");
            printf("|========== Erro 11 - Nome da Function não começou com marcador '!'========| \n");
            printf("|__________________________________________________________________________| \n");
            break;

        case 12:
            printf("|**************************************************************************| \n");
            printf("|========== Erro 12 - Declaração de variável está duplicada ===============| \n");
            printf("|__________________________________________________________________________| \n");
            break;

        case 13:
            printf("|**************************************************************************| \n");
            printf("|========== Erro 13 - Valor da Variavel contem simbolo invalido/aceitável =| \n");
            printf("|__________________________________________________________________________| \n");
            break;

        case 14:
            printf("|**************************************************************************| \n");
            printf("|========== Erro 14 - =====================================================| \n");
            printf("|__________________________________________________________________________| \n");
            break;

        case 15:
            printf("|**************************************************************************| \n");
            printf("|========== Erro 15 - =====================================================| \n");
            printf("|__________________________________________________________________________| \n");
            break;

        default:
            printf("");
    }
    system("pause");
    exit(0);

}
